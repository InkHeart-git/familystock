// 合并所有代码到名称映射
const STOCK_NAME_MAP = {
    ...STOCK_DATABASE.stocks,
    ...STOCK_DATABASE.etfs
};

// 通过代码查找名称（支持A股和ETF）
function getStockNameByCode(code) {
    return STOCK_NAME_MAP[code] || null;
}

// 通过名称查找代码
function getStockCodeByName(name) {
    for (const [code, stockName] of Object.entries(STOCK_NAME_MAP)) {
        if (stockName === name || stockName.includes(name)) {
            return code;
        }
    }
    return null;
}

// 模糊搜索（支持A股和ETF）
function searchStocks(keyword) {
    const results = [];
    const upperKeyword = keyword.toUpperCase();
    
    // 搜索A股
    for (const [code, name] of Object.entries(STOCK_DATABASE.stocks)) {
        if (code.includes(upperKeyword) || name.includes(keyword)) {
            results.push({ code, name, type: 'A股' });
        }
    }
    
    // 搜索ETF
    for (const [code, name] of Object.entries(STOCK_DATABASE.etfs)) {
        if (code.includes(upperKeyword) || name.includes(keyword)) {
            results.push({ code, name, type: 'ETF' });
        }
    }
    
    return results.slice(0, 10); // 最多返回10条
}

// 获取股票类型
function getStockType(code) {
    if (STOCK_DATABASE.stocks[code]) return 'A股';
    if (STOCK_DATABASE.etfs[code]) return 'ETF';
    return '未知';
}

